import { Schema as _Schema, model } from "mongoose";

const Schema = _Schema;

const adminSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default model("Admin", adminSchema);
